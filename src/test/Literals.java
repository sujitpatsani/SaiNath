package test;

public class Literals {

	public static void main(String[] args) {

		double a= 10_0.65_05;
		
		double x;
		System.out.println(x= a+5);
		//Binary Literal by placing '0B/0b' in the beginning
		int s=0B0101;
		//Octal Literal by placing '0' in the beginning
		int d=076545;
		int e=06745543;
		
		//Hexadecimal Literal by placing '0X/0x' in the beginning
		
		int e1=0X9876abe3;
		long e2=0x96eaa336abe3l;
		float f=78.98f;
	}

}
